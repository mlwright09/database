#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*copy;
	unsigned int	len;
	unsigned int	i;

	len = ft_strlen(s1);
	if (!(copy = (char *)malloc(len + 1)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
