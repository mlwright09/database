#include "libft.h"

void	ft_putnbr(int n)
{
	int		num;
	char	digit;

	if (n == -2147483648)
	{
		write(1, "-2", 2);
		n += 2000000000;
		n *= -1;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n > -2147483648 && n <= 2147483647)
	{
		num = n % 10;
		n = n / 10;
		if (n != 0)
			ft_putnbr(n);
		digit = num + 48;
		write(1, &digit, 1);
	}
}
