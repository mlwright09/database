#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		len;

	if (s)
	{
		len = 0;
		while (s[len] != '\0')
			len++;
		write(fd, s, len);
		write(fd, "\n", 1);
	}
}
